export default {
    httpHeader() {
        let header = {};
        /*if (window.$cookies.isKey('access_token')) {
            header = {Authorization: "Bearer " + jwt.verify(cookie.get('access_token'), key).access_token};
        } else {
            header = {}
        }*/
        return {headers: header}
    },
    // retrieve all contact type 
    getAllDiscussions(agenda_id) {
        return new Promise((resolve, reject) => {
            axios.get('/agenda-meeting-discussions/' + agenda_id,this.httpHeader())
            .then(response => {
                resolve(response.data);
            })
            .catch(error => {
                reject(error);
            });
        });
    },
    // get a contact type
    getDiscussion(id) {
        return new Promise((resolve, reject) => {
            axios.get('/meeting-discussions/' + id, this.httpHeader)
            .then(response => {
                resolve(response.data)
            })
            .catch(err => {
                reject(err)
            })
        })
    },
    // create a contact type
    createDiscussion(data) {
        return new Promise((resolve, reject) => {
            axios.post('/meeting-discussions', data, this.httpHeader())
            .then(response => {
                resolve(response.data);
            })
            .catch(err => {
                reject(err);
            })
        });
    },
    //update contact type
    updateDiscussion(data, id) {
        return new Promise((resolve, reject) => {
            axios.put('/meeting-discussions/' + id, data, this.httpHeader())
            .then(response => {
                resolve(response.data)
            })
            .catch(err => {
                reject(err)
            })
        });
    },
    // Delete contact type
    deleteDiscussion(id) {
        return new Promise((resolve, reject) => {
            axios.delete('/meeting-discussions/' + id, this.httpHeader())
            .then(response => {
                resolve(response.data)
            })
            .catch(err => {
                reject(err)
            })
        });
    },
}
